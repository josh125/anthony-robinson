                      <p>
                        <strong>Buyer's Information Sheet</strong> - a form that contains client's essential personal information like contact details, address and etc.<br><br>
                        <strong>Condominium Certificate of Title (CCT)</strong> - a legal document which serves as an evidence of ownership over a condominium unit issued by the Land Registration Authority <br><br>
                        <strong>Contract to Sell (CTS)</strong> - a contract governing a purchase of a specific Unit/House and lot whereby the terms and conditions of the sale, such as but not limited to the payment terms and transfer of ownership, are stated. <br><br>
                        <strong>Deed of Absolute Sale (DAS)</strong> - a contract executed by the seller and the buyer whereby the seller unconditionally transfers to the buyer all its rights, interests and title to the property subject of the sale and the buyer thereby accepts it. <br><br>
                        <strong>Letter of Guarantee (LOG)</strong> - a written guarantee from the bank that the buyer/s? has complied with the bank requirements for the release of loan to cover payment for any portion of the purchase price. <br><br>
                        <strong>Real Estate Property Tax (REPT)</strong> - a tax levied by the Local Government Unit where the property is situated <br><br>
                        <strong>Reservation Agreement</strong> - a document executed by the buyer, declaring his intent to purchase a specific Unit/House and Lot, together with his payment of the Reservation Fee, and subject to the approval by RLC. <br><br>
                        <strong>Reservation Agreement</strong> - a form that consists of property description, contract prices, terms of payment and other terms and conditions that the client had agreed upon relative to the purchase of unit <br><br>
                        <strong>Special Power of Attorney</strong> - an arrangement that grants authority to one person to act in behalf of another. The person giving authorization is called the principal while the person granted authorization to act in behalf of the principal is called attorney-in-fact. This should not be confused with the term attorney-at-law. An attorney-in-fact does not have to be a lawyer; often used when the principal is unable to act for himself because of illness, travel, disability, family problems that require immediate attention, and other similar instance. Through power of attorney, the attorney-in-fact can perform actions that the principal can such as withdrawing funds from your account, signing documents and contracts, and other financial & business actions. <br><br>
                        <strong>Tax Declaration</strong> - a city or municipal document containing the lot/unit number, area, and location of the land/house/condominium unit as well as the name/s, address of the declarant. Total Contract Price - the total amount to which a unit is priced before any applicable discount or rebate is applied<br><br>
                        <strong>Transfer Certificate of Title (TCT)</strong> - a legal document which serves as an evidence of ownership over a parcel of land issued by the Land Registration Authority <br><br>
                        <strong>Transfer Tax and Registration Expense</strong> - the fee assessed by the City Government for the transfer of title from RLC to the buyer/s' name. 
